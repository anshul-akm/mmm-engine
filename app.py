def generate_pdf_report(media_pct, roi_df, insights, filename="Anshulytics_AI_mmm_report.pdf"):

    from reportlab.platypus import (
        SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle, Image
    )
    from reportlab.lib import colors
    from reportlab.lib.styles import getSampleStyleSheet
    from reportlab.lib.units import inch

    doc = SimpleDocTemplate(filename)
    styles = getSampleStyleSheet()
    content = []

    # =========================
    # HEADER (LOGO + TITLE)
    # =========================
    try:
        logo = Image("logo2.png", width=1.2*inch, height=1.2*inch)
        content.append(logo)
    except:
        pass

    content.append(Spacer(1, 10))
    content.append(Paragraph("<b>Anshulytics AI Report</b>", styles["Title"]))
    content.append(Paragraph("Marketing Mix Modeling & Optimization Suite", styles["Normal"]))
    content.append(Spacer(1, 20))

    # =========================
    # CONTRIBUTION TABLE
    # =========================
    content.append(Paragraph("<b>Channel Contribution (%)</b>", styles["Heading2"]))
    content.append(Spacer(1, 10))

    contrib_data = [["Channel", "Contribution (%)"]]

    for ch, val in media_pct.items():
        contrib_data.append([ch, f"{val:.2f}%"])

    table = Table(contrib_data, hAlign='LEFT')
    table.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), colors.black),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
        ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("PADDING", (0, 0), (-1, -1), 8),
    ]))

    content.append(table)
    content.append(Spacer(1, 20))

    # =========================
    # ROI TABLE
    # =========================
    content.append(Paragraph("<b>ROI Analysis</b>", styles["Heading2"]))
    content.append(Spacer(1, 10))

    roi_data = [["Channel", "ROI"]]

    for ch, val in roi_df.items():
        roi_data.append([ch, f"{val:.2f}"])

    table = Table(roi_data, hAlign='LEFT')
    table.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), colors.darkblue),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
        ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("PADDING", (0, 0), (-1, -1), 8),
    ]))

    content.append(table)
    content.append(Spacer(1, 20))

    # =========================
    # INSIGHTS
    # =========================
    content.append(Paragraph("<b>Key Insights</b>", styles["Heading2"]))
    content.append(Spacer(1, 10))

    if insights:
        for ins in insights:
            content.append(Paragraph(f"• {ins}", styles["Normal"]))
            content.append(Spacer(1, 6))
    else:
        content.append(Paragraph("No insights generated", styles["Normal"]))

    content.append(Spacer(1, 30))

    # =========================
    # FOOTER
    # =========================
    content.append(Paragraph(
        "Generated by Anshulytics AI",
        styles["Italic"]
    ))

    # Build PDF
    doc.build(content)

    with open(filename, "rb") as f:
        return f.read()

